#Use Hash.new to create a new hash called pets
pets = Hash.new
#Add a pet to your pets hash. It can be any key-value pair you like!
pets["pet"] = 'dog'

#Access the key-value pair you added to pets, just like step 2 in the example above.
#Use puts to print that value 
pets = {
  "pet" => "dog"
}
puts pets ["pet"]
# will print "dog"

